    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String desc = jTextField1.getText();
        String price = jTextField2.getText();

        if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("0|[1-9]|[1-9][0-9]*").matcher(price).matches()) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Amount", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs2 = MySQL.search("SELECT * FROM `subject` WHERE `name` = '" + desc + "' AND `price` = '" + price + "'");

                if (rs2.next()) {
                    JOptionPane.showMessageDialog(this, "Subject already added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `subject` (`name`,`price`) VALUES ('" + desc + "','" + price + "')");
                    JOptionPane.showMessageDialog(this, "Subject added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            loadSubjects();
            jButton1.setEnabled(false);
            jButton3.setEnabled(false);

            resetfield();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

