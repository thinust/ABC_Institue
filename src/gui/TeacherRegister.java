    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String mobile = jTextField3.getText();
        String email = jTextField4.getText();
        String city = jLabel9.getText();
        String adid = jLabel7.getText();
        String subId = jLabel11.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Student Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Mobile No ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid mobille number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("^[a-z0-9._%+-]+@[a-z0-9.-]+\\.[a-z]{2,6}$").matcher(email).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Email Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (adid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Select Address ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subId.equals("None")) {
            JOptionPane.showMessageDialog(this, "Select Subject ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT `id` FROM `city` WHERE `name`='" + city + "'");
                rs1.next();
                String city_id = rs1.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `teacher` WHERE `teacher`.`email`='" + email + "'");

                if (rs2.next()) {
                    JOptionPane.showMessageDialog(this, "Teacher Details already added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `teacher` (`teacher`.`name`,`teacher`.`email`,`teacher`.`mobile_no`,`teacher`.`address_id`,`teacher`.`subject_id`) VALUES ('" + name + "','" + email + "','" + mobile + "','" + adid + "','" + subId + "')");
                    JOptionPane.showMessageDialog(this, "Teacher Details added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            jButton2.setEnabled(false);
            resetField();
            loadTeachers();

        }

    }//GEN-LAST:event_jButton2ActionPerformed

