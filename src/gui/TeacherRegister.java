    public void loadTeachers() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `teacher` JOIN `subject` ON teacher.subject_id=subject.id JOIN `address` ON teacher.address_id=address.id JOIN `city` ON address.city_id=city.id  ORDER BY teacher.id ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("teacher.id"));
                v.add(rs.getString("teacher.name"));
                v.add(rs.getString("teacher.email"));
                v.add(rs.getString("teacher.mobile_no"));
                String address = rs.getString("address.line1") + "," + rs.getString("address.line2") + "," + rs.getString("city.name");

                v.add(address);
                v.add(rs.getString("subject.name"));
                v.add(rs.getString("subject.price"));
                dtm.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

