    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String email = jTextField4.getText();
        String mobile = jTextField3.getText();
        int r = jTable1.getSelectedRow();
        String tId = jTable1.getValueAt(r, 0).toString();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Teacher Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Mobile No", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs2 = MySQL.search("SELECT * FROM `teacher` WHERE `teacher`.`email`='" + email + "' ");

                if (rs2.next()) {

                    MySQL.iud("UPDATE `teacher` SET `mobile_no`= '" + mobile + "' WHERE `id`='" + tId + "'");
                    JOptionPane.showMessageDialog(this, "Teacher Details Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {

                    MySQL.iud("UPDATE `teacher` SET `email`= '" + email + "' , `mobile_no`= '" + mobile + "' WHERE `id`='" + tId + "'");
                    JOptionPane.showMessageDialog(this, "Teacher Details Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            jButton3.setEnabled(false);
            resetField();
            loadTeachers();

        }
    }//GEN-LAST:event_jButton3ActionPerformed


