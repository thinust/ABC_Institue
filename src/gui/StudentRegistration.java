    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String mobile_no = jTextField3.getText();
        String email = jTextField4.getText();
        String adl1 = jLabel8.getText();
        String adl2 = jLabel10.getText();
        String city = jLabel9.getText();
        String adid = jLabel7.getText();

        Date dob = jDateChooser1.getDate();
        String dob2 = null;

        if (jDateChooser1.getDate() != null) {

            dob2 = sdf2.format(jDateChooser1.getDate());
        }

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Student Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile_no == null) {
            JOptionPane.showMessageDialog(this, "Enter Student Mobile No", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email == null) {
            JOptionPane.showMessageDialog(this, "Enter Student Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (dob == null) {
            JOptionPane.showMessageDialog(this, "Enter Student Date of Birth", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (dob.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Invalid Date of Birth", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (adid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Select Student Address ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT `id` FROM `city` WHERE `name`='" + city + "'");
                rs1.next();
                String city_id = rs1.getString("id");

                ResultSet rs4 = MySQL.search("SELECT * FROM `address` \n"
                        + "WHERE  address.line1='" + adl1 + "' AND address.line2='" + adl2 + "' AND address.city_id='" + city_id + "'");
                rs4.next();
                String address_id = rs4.getString("id");

                ResultSet rs3 = MySQL.search("SELECT COUNT(student.id) FROM student;");
                rs3.next();
                System.out.println(rs3.getString("COUNT(student.id)"));

                String stCount = rs3.getString("COUNT(student.id)");

                String stId = stCount + dob2 + stCount;

                ResultSet rs2 = MySQL.search("SELECT * FROM `student` JOIN `address` ON `student`.`address_id`=`address`.`id` \n"
                        + "WHERE `student`.`name`='" + name + "' AND student.dob='" + sdf.format(dob) + "' AND `address`.`line1`='" + adl1 + "' AND address.line2='" + adl2 + "' AND address.city_id='" + city_id + "'");

                ResultSet rs5 = MySQL.search("SELECT * FROM student WHERE student.email='" + email + "'");

                if (rs2.next()) {

                    JOptionPane.showMessageDialog(this, "Student Details already added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (rs5.next()) {
                    JOptionPane.showMessageDialog(this, "Student Email Already Used", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `student` (`student`.`id`,`student`.`name`,`student`.`dob`,`student`.`mobile_no`,`student`.`email`,`student`.`address_id`) VALUES ('" + stId + "','" + name + "','" + sdf.format(dob) + "','" + mobile_no + "','" + email + "','" + address_id + "')");
                    JOptionPane.showMessageDialog(this, "Student Details added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            resetField();
            
            loadStudents();

        }

    }//GEN-LAST:event_jButton2ActionPerformed

