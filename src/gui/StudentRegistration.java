    public void loadStudents() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `student` JOIN `address` ON `student`.`address_id`=`address`.`id` JOIN `city` ON `address`.`city_id`=`city`.`id` ORDER BY `student`.`id` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("student.id"));
                v.add(rs.getString("student.name"));
                v.add(rs.getString("student.dob"));
                v.add(rs.getString("student.email"));
                v.add(rs.getString("student.mobile_no"));
                String address = rs.getString("address.line1") + "," + rs.getString("address.line2") + "," + rs.getString("city.name");

                v.add(address);
                dtm.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

