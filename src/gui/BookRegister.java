    public void loadBooks() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `product` JOIN category ON product.category_id=category.id ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("no"));
                v.add(rs.getString("name"));
                v.add(rs.getString("category.name"));
                dtm.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadBooks(String text) {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `product` JOIN category ON product.category_id=category.id WHERE product.name LIKE '" + text + "%' OR product.no LIKE '" + text + "%' ORDER BY `no` ASC ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("no"));
                v.add(rs.getString("name"));
                v.add(rs.getString("category.name"));   
                dtm.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCategory() {

        try {
            ResultSet rs1 = MySQL.search("SELECT * FROM `category`");

            Vector v = new Vector();
            v.add("Select");

            while (rs1.next()) {
                v.add(rs1.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
        }
    }

