    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String desc = jTextField1.getText();
        String No = jTextField4.getText();
        String category = jComboBox1.getSelectedItem().toString();

        if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (No.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Product No", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT id FROM `category` WHERE `name` = '" + category + "'");
                rs1.next();

                String category_id = rs1.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `product` WHERE `no` = '" + No + "';");

                if (rs2.next()) {
                    JOptionPane.showMessageDialog(this, "Product No already Used", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `product` (`no`,`name`,`category_id`) VALUES ('" + No + "','" + desc + "','" + category_id + "')");
                    JOptionPane.showMessageDialog(this, "Book added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            loadBooks();
            jButton1.setEnabled(false);
            jButton3.setEnabled(false);

            resetfield();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

