    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String bdesc = jTextField1.getText();
        String bno = jTextField4.getText();

        if (bdesc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs2 = MySQL.search("SELECT * FROM `product` WHERE name = '" + bdesc + "'");

                if (rs2.next()) {
                    JOptionPane.showMessageDialog(this, "Book already added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("UPDATE `product` SET `name`= '" + bdesc + "'  WHERE `no`='" + bno + "'");
                    JOptionPane.showMessageDialog(this, "Book update successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            jTextField4.setEnabled(true);
            jComboBox1.setEnabled(true);
            loadBooks();
            resetfield();

            jButton3.setEnabled(false);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

