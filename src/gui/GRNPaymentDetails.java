    public void loadGRN() {

        try {

            rs = MySQL.search("SELECT * FROM grn JOIN grn_item ON grn.id=grn_item.grn_id JOIN stock ON grn_item.stock_id=stock.id JOIN product ON product.id=stock.product_id ORDER BY grn.id ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("grn.id"));
                v.add(rs.getString("grn.date_time"));
                v.add(rs.getString("product.id"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("grn_item.buing_price"));
                v.add(rs.getString("grn_item.qty"));
                double itemtotal = Integer.parseInt(rs.getString("grn_item.qty")) * Double.parseDouble(rs.getString("grn_item.buing_price"));
                v.add(df.format(itemtotal));
                dtm.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

