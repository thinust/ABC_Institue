    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        Vector v = null;
        HashMap parameter = null;
        JasperPrint jp = null;
        JRBeanCollectionDataSource datasource = null;
        InputStream pathStream = null;

        String type = jComboBox1.getSelectedItem().toString();

        try {
            pathStream = StudentPayment.class.getResourceAsStream("/report/GRN_Report.jasper");

            v = new Vector();

            for (int i = 0; i < jTable1.getRowCount(); i++) {

                String grnNo = jTable1.getValueAt(i, 0).toString();
                String dateTime = jTable1.getValueAt(i, 1).toString();
                String pId = jTable1.getValueAt(i, 2).toString();
                String pName = jTable1.getValueAt(i, 3).toString();
                String uniPrice = jTable1.getValueAt(i, 4).toString();
                String qty = jTable1.getValueAt(i, 5).toString();
                String total = jTable1.getValueAt(i, 6).toString();

                parameter = new HashMap();
                v.add(new grnReport(grnNo, dateTime, pId, pName, uniPrice, qty, total));
            }

            datasource = new JRBeanCollectionDataSource(v);

            jp = JasperFillManager.fillReport(pathStream, parameter, datasource);

            if (type.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Slect Type", "Warrning", JOptionPane.WARNING_MESSAGE);
            } else if (type.equals("View")) {
                JasperViewer jv = new JasperViewer(jp, false);
                jv.setVisible(true);
            } else if (type.equals("Print")) {
                JasperPrintManager.printReport(jp, true);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton4ActionPerformed

